    private HashMap<Integer, Integer> makeMappings() {
        // Declare a HashMap to hold the results
        HashMap<Integer, Integer> mappings = new HashMap<>();
        // Get a list of all fields
        HashMap<Integer, javax.swing.JTextPane> fields = getCoordFields();
        // Get the keys for the fields
        Set<Integer> fieldKeys = fields.keySet();
        // For each field in fields
        for(Integer key : fieldKeys) {
            javax.swing.JTextPane field = fields.get(key);
            // If the field is not blank,
            if (!field.getText().isEmpty()) {
                int number = Integer.parseInt(field.getText());
                mappings.put(key, number);
            }
        }
        return mappings;
    }
    
