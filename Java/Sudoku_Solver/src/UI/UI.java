    private void setDialogPosition() {
        int uiXpos = this.getLocation().x;
        int uiYpos = this.getLocation().y;
        int uiHeight = this.getSize().height;
        int uiWidth = this.getSize().width;
        int sdHeight = successDialog.getSize().height;
        int sdWidth = successDialog.getSize().width;
        int sdXpos = ((uiXpos + uiWidth)/2)-(sdWidth/2);
        int sdYpos = ((uiYpos + uiHeight)/2)-(sdHeight/2);
        successDialog.setLocation(sdXpos, sdYpos);
    }
    
    
    private void formatTextAreas() {
        // Change the font for text fields
        HashMap<Integer, javax.swing.JTextPane> fields = getCoordFields();
        Set<Integer> fieldKeys = fields.keySet();
        Font font = new Font("Verdana",Font.PLAIN, 25);
        for(int key : fieldKeys) {
            javax.swing.JTextPane field = fields.get(key);
            field.setFont(font);
            
            // try centerring
            StyledDocument doc = field.getStyledDocument();
            SimpleAttributeSet center = new SimpleAttributeSet();
            StyleConstants.setAlignment(center, StyleConstants.ALIGN_CENTER);
            doc.setParagraphAttributes(0, doc.getLength(), center, false);
        }
    }
    
    private void tryChangeTabBehaviour() {
        HashMap<Integer, javax.swing.JTextPane> fields = getCoordFields();
        Set<Integer> fieldKeys = fields.keySet();
        for(int key : fieldKeys) {
            Component field = fields.get(key);
            field.setFocusTraversalKeys(KeyboardFocusManager.FORWARD_TRAVERSAL_KEYS, null);
            field.setFocusTraversalKeys(KeyboardFocusManager.BACKWARD_TRAVERSAL_KEYS, null);
        }
    }

